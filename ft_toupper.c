#include "libft.h"

int	ft_toupper(int c)
{
	if (c >= 97 && c <= 122)
		return (c - 32);
	return (0);
}
int	main(void)
{
	char	a;

	a = 'a';
	printf("%c", ft_toupper(a));
}
